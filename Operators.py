print("\n---------> Assignments Operator <---------------------")
print(f"The value of a == b: {a==b}")
print(f"The value of a != b: {a!=b}")
print(f"The value of a > b: {a>b}")
print(f"The value of a < b: {a<b}")
print(f"The value of a >= b: {a>=b}")

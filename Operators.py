print("\n---------> Membership Operator <---------------------")
c=[1,2,3,4]
print(f"The value of a in c: {a in c}")
print(f"The value of a not in c: {a not in c}")

print("\n---------> Logical Operator <---------------------")

print(f"The value of 1<2 and 2<3 and 3<4: {1<2 and 2<3 and 3>4}")
#                     OR
print(f"The value of all([1<2,2<3,3<4]): {all([1<2,2<3,3>4])}")

print(f"The value of 1<2 or 2<3 or 3<4: {1<2 or 2<3 or 3>4}")
#                     OR
print(f"The value of any([1<2,2<3,3<4]): {any([1<2,2<3,3>4])}")














